Translate Tool by Martyn Bissett.

Usage: cattlog [command] [args...] [options]

Commands:

  init                                 Creates a cattlog.json config file
  scan <lang>                          Preview changes that update will make
  update <lang>                        Update files containing keys
  get_value <lang> <key>               Get an individual value by key
  set_value <lang> <key>="<value>"     Set an individual value by key
  count <lang>                         Return total
